CREATE DATABASE book_Store;
USE book_Store;
-- Table named books has already been created and data has been filled

-- CONCAT
SELECT CONCAT(Author_fname,'!!!') FROM books; -- Concatenate authore name with !!!
SELECT CONCAT(Author_fname, NULL) AS Author FROM books; -- Concatenate with NULL = NULL
-- Use aliases for better reading
SELECT CONCAT(author_fname,' ',author_lname) AS "Author\"s Name" FROM books;
-- CONCAT_WS
SELECT CONCAT_WS('---',author_fname,author_lname) FROM books; -- 'Concat with' operator.
-- CONCAT END

-- SUBSTRING
-- SELECT SUBSTIRNG(TEXT, STARING INDEX, LENGTH) INDEX START FROM 1 NOT 0.
SELECT SUBSTRING('HELLO WORLD',1,4);
SELECT SUBSTRING('HELLO WORLD',5); -- IF WE DON'T PROVIDE TH LENGHT, IT WILL GO TILL THE END
SELECT SUBSTRING('HELLO WORLD',-6,4); -- START THE INDEXING FROM THE END OF STRING AND THEN GO FORWARD

-- FIRST CHARACTER OF AUTHOR LAST NAME
SELECT SUBSTRING(author_lname,1,1) FROM books;

-- SUBSTR() IS SYNONYMS FOR SUBSTRING()
SELECT SUBSTR('HELLO WORLD',2,5);

-- SUBTRING WITH FROM AND FOR
SELECT SUBSTR('HELLO WORLD' FROM 3);
SELECT SUBSTR('HELLO WORLD' FROM 3 FOR 5);
  
-- SUBSTRING End

-- COMBINING SUBSTRING AND CONCAT
SELECT CONCAT(SUBSTR(title,1,10),'...') AS Short_Title FROM books;
-- COMBINING SUBSTRING AND CONCAT END

