-- CHAR_LENGHT AND LENGHT
SELECT CHAR_LENGTH('DOLPHIN'); -- WILL COUNT LENGHT BY NUMBER OF CHARACTER
SELECT LENGTH('HELLO'); -- WILL COUNT LENGHT BY NUMBER OF BYTES
SELECT CHAR_LENGTH(title) AS length, title FROM books;
SELECT CONCAT(
	author_lname, ' is ', CHAR_LENGTH(author_lname), ' characters long'
    ) AS 'Last name character info'
	FROM books;
-- CHAR_LENGHT AND LENGHT END 

